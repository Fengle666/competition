public enum ContestStateEnum {

    SIGN(1,"报名中"),

    CONTESTING(2,"竞赛中"),

    OVER(3,"已结束"),

    ;

    Integer code;

    String desc;

    ContestStateEnum(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
